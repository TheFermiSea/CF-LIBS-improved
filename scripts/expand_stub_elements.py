def expand_stubs(db_path: Path, dry_run: bool = False) -> dict[str, int]:
    if not db_path.exists():
        raise FileNotFoundError(f"Database not found: {db_path}")
    conn = sqlite3.connect(str(db_path))
    cur = conn.cursor()

    targets = sorted(set(L[0] for L in LINES))

    # BEFORE counts
    before = {}
    for el in targets:
        before[el] = cur.execute(
            "SELECT COUNT(*) FROM lines WHERE element=?", (el,)
        ).fetchone()[0]

    cur.execute("SELECT MAX(id) FROM lines")
    next_id = (cur.fetchone()[0] or 0) + 1

    inserted = 0
    skipped = 0
    for elem, sp, wl, aki, ei, ek, gi, gk, rel_int, grade in LINES:
        is_res = 1 if abs(ei) < 0.05 else 0
        aki_unc = {"A": 0.03, "A+": 0.03, "B": 0.10, "B+": 0.07, "C": 0.25, "C+": 0.18, "D": 0.50}.get(grade, 0.30)
        cur.execute(
            """
            INSERT OR IGNORE INTO lines
            (id, element, sp_num, wavelength_nm, aki, ei_ev, ek_ev, gi, gk, rel_int,
             stark_w, stark_alpha, stark_shift, is_resonance, aki_uncertainty, accuracy_grade)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, NULL, NULL, NULL, ?, ?, ?)
            """,
            (next_id, elem, sp, wl, aki, ei, ek, gi, gk, rel_int, is_res, aki_unc, grade),
        )
        if cur.rowcount:
            inserted += 1
            next_id += 1
        else:
            skipped += 1

    if not dry_run:
        conn.commit()
    else:
        conn.rollback()

    print(f"{'Element':<10}{'BEFORE':>10}{'AFTER':>10}{'TARGET':>10}")
    for el in targets:
        after = cur.execute(
            "SELECT COUNT(*) FROM lines WHERE element=?", (el,)
        ).fetchone()[0]
        flag = " OK" if after >= 50 else " UNDER"
        print(f"{el:<10}{before[el]:>10}{after:>10}{50:>10}{flag}")

    print()
    print(f"Total inserted={inserted} skipped(dup)={skipped}")
    conn.close()
    return {"inserted": inserted, "skipped": skipped}


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__)
