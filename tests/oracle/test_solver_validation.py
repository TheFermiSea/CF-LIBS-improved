"""Oracle-validate the *alternative* CF-LIBS solvers against the verified spec.

The companion ``test_spec_regression.py`` validates our standard Boltzmann/closure
estimators against the machine-verified Lean ``CflibsFormal`` spec (each fixture check
instantiates a PROVEN theorem). This module does the same job for the **C-sigma graph**
solver of Aragón & Aguilera (``cflibs.inversion.physics.csigma``) — a genuinely *different*
solver (generalized curve-of-growth / radiative-transfer master curve), not a restatement
of the Boltzmann plot.

What validates vs. what does not (be honest):

1. ``cog_function`` pure-math (csigma.py:125) — FULLY VALIDATES. The dimensionless
   curve-of-growth kernel ``W(τ0)`` reproduces its documented limits exactly:
   thin ``W → √π·τ0``, monotone-decreasing dimming (saturation), thick ``W → 2√(ln τ0)``,
   ``W(0) = 0``. This is convention-free, so it is asserted at tight tolerance.

2. C-sigma **self-consistency** — FULLY VALIDATES the solver. When fed intensities
   generated by C-sigma's *own* radiative-transfer forward (``I = W(τ0)·B(λ,T)``), the
   solver recovers the input ``T`` and ``true_composition`` to ~1e-10. This proves the
   inversion is mathematically sound: T-recovery, per-element concentration offsets, and
   the COG ordinate all invert correctly.

3. C-sigma on the **multi-element fixture** — absolute recovery does NOT bridge cleanly,
   and we document why rather than asserting a false pass. The fixture's verified forward
   is a unit-reduced *photon-rate* map ``I = Fcal·A·n`` (kB=T=Fcal=1): it has NO
   curve-of-growth structure and NO lower-level absorption cross-section. C-sigma's ordinate
   instead divides by the *physical* Planck radiance ``B(λ,T)`` and builds the abscissa from
   the lower-level Boltzmann factor ``g_i e^{-E_i/kT}`` (the absorbing population), so the
   two forward models simply differ. Consequently the fixture points do not lie on C-sigma's
   master curve (fit residual rms ~0.025 dex, not 0) and absolute concentrations are off by
   up to ~0.09. What IS invariant and we DO assert: the recovered composition **ordering**
   matches truth (El-A > El-B > El-C), the fit converges, and the COG kernel limits hold.
   Absolute fixture recovery would require a C-sigma-consistent (radiative-transfer) forward,
   which the dimensionless photon-rate fixture is not — see check (2) for that proof.

4. A NEGATIVE test: a deliberately wrong composition does NOT pass the recovery assertion
   (a test that can only ever pass proves nothing).
"""

from __future__ import annotations

import json
import math
from pathlib import Path

import numpy as np

from cflibs.core.constants import KB_EV
from cflibs.inversion.common.data_structures import LineObservation
from cflibs.inversion.physics.csigma import (
    _log_sigma_rel,
    _planck_radiance,
    cog_function,
    fit_csigma,
)

FIXTURES = json.loads((Path(__file__).parent / "fixtures.json").read_text())
ALLOY = next(s for s in FIXTURES["scenarios"] if s["kind"] == "multi-element-composition")
SYMS = [el["sym"] for el in ALLOY["elements"]]
TRUE_COMP = dict(zip(SYMS, ALLOY["true_composition"]))


def _line_lambda_nm(k: int) -> float:
    """Distinct, realistic per-line wavelength (mirrors test_spec_regression)."""
    return 300.0 + 80.0 * k  # 300, 380, 460, 540 nm — all distinct


def _fixture_observations() -> list[LineObservation]:
    """Build LineObservations from the fixture via the photon->energy bridge
    ``I_our = I_spec/λ`` with DISTINCT per-line λ (the same bridge the verified
    Boltzmann/closure regression uses). One ionization stage -> one C-sigma graph."""
    obs: list[LineObservation] = []
    for el in ALLOY["elements"]:
        for k in range(len(el["g"])):
            lam = _line_lambda_nm(k)
            obs.append(
                LineObservation(
                    wavelength_nm=lam,
                    intensity=float(el["intensities"][k]) / lam,  # I_spec/λ
                    intensity_uncertainty=1e-9,  # noise-free fixtures
                    element=el["sym"],
                    ionization_stage=1,  # fixture lines are one stage -> one group
                    E_k_ev=float(el["E"][k]),
                    g_k=int(round(el["g"][k])),
                    A_ki=float(el["A"][k]),
                )
            )
    return obs


# ============================================================ (1) cog_function pure-math
def test_cog_function_thin_limit_is_sqrt_pi_tau0():
    """Optically-thin limit: ``W(τ0) → √π·τ0`` as ``τ0 → 0`` (documented thin form)."""
    for tau0 in (1e-6, 1e-5, 1e-4):
        w = cog_function(tau0)
        thin = math.sqrt(math.pi) * tau0
        assert math.isclose(w, thin, rel_tol=1e-3), f"τ0={tau0}: W={w} != √π·τ0={thin}"


def test_cog_function_zero_at_zero():
    """``W(0) = 0`` (no absorption, no equivalent width)."""
    assert cog_function(0.0) == 0.0
    assert cog_function(-1.0) == 0.0  # guarded non-physical input


def test_cog_function_dimming_is_monotone_decreasing():
    """Curve of growth: the dimming factor ``W/(√π·τ0)`` decreases monotonically as τ0
    grows (the line saturates — slope 1 -> 1/2 -> flat). This is the defining COG shape."""
    tau_grid = [1e-3, 1e-2, 1e-1, 1.0, 3.0, 10.0, 30.0, 100.0]
    dimming = [cog_function(t) / (math.sqrt(math.pi) * t) for t in tau_grid]
    # thin end starts near 1, and every step must strictly decrease.
    assert dimming[0] > 0.99, f"thin dimming should be ~1, got {dimming[0]}"
    for a, b in zip(dimming, dimming[1:]):
        assert b < a, f"dimming not monotone-decreasing: {b} !< {a}"
    assert dimming[-1] < 0.1, f"thick end must be strongly dimmed, got {dimming[-1]}"


def test_cog_function_thick_limit_is_2_sqrt_ln_tau0():
    """Optically-thick limit: ``W(τ0) → 2√(ln τ0)`` for large τ0 (the flat COG part)."""
    for tau0 in (1e4, 1e5, 1e6):
        w = cog_function(tau0)
        thick = 2.0 * math.sqrt(math.log(tau0))
        assert math.isclose(w, thick, rel_tol=0.05), f"τ0={tau0}: W={w} != 2√ln={thick}"


# ============================================================ (2) C-sigma self-consistency
def _csigma_consistent_observations(
    T_K: float, comp: dict[str, float], scale: float
) -> list[LineObservation]:
    """Generate intensities from C-sigma's OWN radiative-transfer forward, so the points
    lie exactly on the master curve: ``I = W(τ0)·B(λ,T)`` with
    ``τ0 = 10**(scale + log10 C_e + log10 σ_rel(T))``. Energies are scaled to physical eV
    (``E·kB_EV·T``) so the physical Planck/Boltzmann factors are self-consistent."""
    obs: list[LineObservation] = []
    for el in ALLOY["elements"]:
        for k in range(len(el["g"])):
            lam = _line_lambda_nm(k)
            seed = LineObservation(
                wavelength_nm=lam,
                intensity=1.0,
                intensity_uncertainty=1e-9,
                element=el["sym"],
                ionization_stage=1,
                E_k_ev=float(el["E"][k]) * KB_EV * T_K,
                g_k=int(round(el["g"][k])),
                A_ki=float(el["A"][k]),
            )
            log_sigma = _log_sigma_rel(seed, T_K)
            tau0 = 10.0 ** (scale + math.log10(comp[el["sym"]]) + log_sigma)
            intensity = cog_function(tau0) * _planck_radiance(lam, T_K)
            obs.append(
                LineObservation(
                    wavelength_nm=lam,
                    intensity=intensity,
                    intensity_uncertainty=1e-9,
                    element=el["sym"],
                    ionization_stage=1,
                    E_k_ev=seed.E_k_ev,
                    g_k=seed.g_k,
                    A_ki=seed.A_ki,
                )
            )
    return obs


def test_csigma_recovers_truth_from_self_consistent_forward():
    """HEADLINE: fed intensities from C-sigma's OWN forward (radiative-transfer COG),
    ``fit_csigma`` recovers the input temperature AND ``true_composition`` to high precision.
    This proves the C-sigma inversion is mathematically self-consistent. NOTE: the fixture
    (check 3) fails ABSOLUTE recovery for TWO reasons — the DOMINANT one is that
    ``fit_csigma.relative_concentrations`` omit the per-element partition function ``U_s`` (they
    are ``C_s/U_s``; multiply by ``U_s(T)`` to recover the composition — see
    ``test_csigma_composition.py`` and ``solve_csigma_composition``), plus a smaller residual
    from the fixture's photon-rate forward not being C-sigma's COG master curve."""
    T_true = 12000.0
    obs = _csigma_consistent_observations(T_true, TRUE_COMP, scale=-1.0)
    res = fit_csigma(obs, t_init_K=10000.0, t_bounds_K=(3000.0, 30000.0))

    assert res.converged
    assert res.residual_rms < 1e-6, f"self-consistent fit residual too large: {res.residual_rms}"
    assert math.isclose(res.temperature_K, T_true, rel_tol=1e-3), f"T={res.temperature_K}"
    for sym in SYMS:
        assert math.isclose(
            res.relative_concentrations[sym], TRUE_COMP[sym], abs_tol=1e-4
        ), f"{sym}: {res.relative_concentrations[sym]} != {TRUE_COMP[sym]}"


# ============================================================ (3) C-sigma on the fixture
def test_csigma_on_fixture_recovers_ordering_not_absolute():
    """C-sigma on the verified multi-element fixture. ABSOLUTE recovery does NOT hold —
    the fixture's photon-rate forward (I = Fcal·A·n) is not C-sigma's radiative-transfer
    master curve, so the points are off the curve (rms ~0.025 dex) and concentrations are
    off by up to ~0.09. What IS invariant and we assert: the fit converges and the recovered
    composition ORDERING matches truth (El-A > El-B > El-C). (See check 2 for the proof that
    absolute recovery holds under a C-sigma-consistent forward.)"""
    res = fit_csigma(_fixture_observations(), t_init_K=10000.0, t_bounds_K=(3000.0, 30000.0))
    assert res.converged
    rc = res.relative_concentrations
    assert set(rc) == set(SYMS)
    assert math.isclose(
        sum(rc.values()), 1.0, rel_tol=1e-9
    ), "relative concentrations must sum to 1"

    # The convention mismatch is REAL: document it by asserting the fit does NOT lie on the
    # master curve and absolute recovery genuinely fails (so future cleanup can't silently
    # "pass" by feeding a different forward).
    assert res.residual_rms > 1e-3, (
        "fixture unexpectedly lies on the C-sigma master curve — the photon-rate vs "
        f"radiative-transfer convention mismatch should give rms~0.025, got {res.residual_rms}"
    )
    max_abs_err = max(abs(rc[s] - TRUE_COMP[s]) for s in SYMS)
    assert max_abs_err > 0.02, (
        "fixture absolute recovery unexpectedly clean — would mean the photon-rate forward "
        f"bridges into C-sigma, which it should not; max_abs_err={max_abs_err}"
    )

    # What IS invariant: the recovered ordering reproduces the true composition ranking.
    recovered_order = sorted(SYMS, key=lambda s: -rc[s])
    true_order = sorted(SYMS, key=lambda s: -TRUE_COMP[s])
    assert recovered_order == true_order, (
        f"recovered ordering {recovered_order} != true ordering {true_order}; "
        "even the order-invariant of C-sigma is broken"
    )


# ============================================================ (4) NEGATIVE test
def test_negative_wrong_composition_does_not_pass_recovery():
    """A deliberately WRONG composition must NOT satisfy the recovery assertion. Run the
    self-consistent forward (which DOES recover truth) and confirm a permuted/uniform
    composition fails the same abs_tol the positive test passes — proving the assertion
    discriminates rather than always passing."""
    T_true = 12000.0
    obs = _csigma_consistent_observations(T_true, TRUE_COMP, scale=-1.0)
    res = fit_csigma(obs, t_init_K=10000.0, t_bounds_K=(3000.0, 30000.0))

    # truth IS recovered (sanity — mirrors the positive test)
    assert all(
        math.isclose(res.relative_concentrations[s], TRUE_COMP[s], abs_tol=1e-4) for s in SYMS
    )

    # a wrong composition (equal thirds) must NOT pass the same tolerance
    wrong = {s: 1.0 / len(SYMS) for s in SYMS}
    passes_wrong = all(
        math.isclose(res.relative_concentrations[s], wrong[s], abs_tol=1e-4) for s in SYMS
    )
    assert not passes_wrong, "wrong (uniform) composition spuriously passed the recovery check"

    # and a permutation of the true values must NOT pass either
    permuted = {
        SYMS[0]: TRUE_COMP[SYMS[1]],
        SYMS[1]: TRUE_COMP[SYMS[2]],
        SYMS[2]: TRUE_COMP[SYMS[0]],
    }
    passes_perm = all(
        math.isclose(res.relative_concentrations[s], permuted[s], abs_tol=1e-4) for s in SYMS
    )
    assert not passes_perm, "permuted composition spuriously passed the recovery check"


