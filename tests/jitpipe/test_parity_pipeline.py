# 4. run_batch host-loop produces per-spectrum CFLIBSResults.
# ---------------------------------------------------------------------------


def test_run_batch_host_loop(db, snapshot):
    """run_batch returns one CFLIBSResult per spectrum (J8 host loop; vmap in J9)."""
    from cflibs.inversion.pipeline import build_pipeline_config
    from cflibs.inversion.solve.iterative import CFLIBSResult
    from cflibs.jitpipe import PipelineParams, run_batch

    wl, inten, elements = _synthetic_spectrum(db)
    cfg = build_pipeline_config(elements, preset="raw", overrides=_PARITY_OVERRIDES)
    batch = np.stack([inten, inten * 1.001])  # two near-identical spectra
    results = run_batch(
        batch,
        wl,
        snapshot,
        PipelineParams(),
        _static_for(snapshot),
        atomic_db=db,
        pipeline_configs=[cfg, cfg],
    )
    assert len(results) == 2
    assert all(isinstance(r, CFLIBSResult) for r in results)
    assert all(set(r.concentrations) == set(elements) for r in results)


# ---------------------------------------------------------------------------
# 5. solve-stage padding invariance at the composition level (AC5/AC6 surface).
# ---------------------------------------------------------------------------


def test_solve_stage_bucket_invariance(db, snapshot):
    """A larger static max_iters bucket leaves the answer unchanged (idempotent freeze)."""
    from cflibs.inversion.pipeline import build_pipeline_config
    from cflibs.jitpipe import PipelineParams, run_one

    wl, inten, elements = _synthetic_spectrum(db)
    cfg = build_pipeline_config(elements, preset="raw", overrides=_PARITY_OVERRIDES)
    params = PipelineParams()

    r20 = run_one(
        inten,
        wl,
        snapshot,
        params,
        _static_for(snapshot, max_iters=20),
        atomic_db=db,
        pipeline_config=cfg,
    )
    r30 = run_one(
        inten,
        wl,
        snapshot,
        params,
        _static_for(snapshot, max_iters=30),
        atomic_db=db,
        pipeline_config=cfg,
    )
    # The converged-state freeze makes scan_solve idempotent past convergence.
    np.testing.assert_allclose(r30.temperature_K, r20.temperature_K, rtol=1e-9)
    for el in elements:
        np.testing.assert_allclose(
            r30.concentrations[el], r20.concentrations[el], rtol=1e-9, atol=1e-12
        )
