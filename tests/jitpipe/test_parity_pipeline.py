"""End-to-end parity tests for the J8 jit pipeline composition (ADR-0004 §8.1, M1).

Feeds IDENTICAL inputs to :func:`cflibs.jitpipe.run_one` and the REAL reference
:func:`cflibs.inversion.pipeline.run_pipeline` and asserts the M1 tolerances:

* element calls agree (>= 95 % of spectra; here every element call must match),
* T within 2 %, n_e within 10 %,
* per-element concentration rtol 5 % / atol 0.01,
* failure-policy parity: a spectrum with zero usable lines produces the same
  all-FN scored record on both pipelines (the reference raises ``ValueError``;
  the jit pipeline returns an all-zero ``CFLIBSResult`` with ``converged=False``).

Parity-config note
------------------
The production geological preset routes the reference solve through the
SB-graph Python path (``saha_boltzmann_graph=True`` + ``stark_ne=True`` force
``_solve_python``). The J8 jit solve spine (``scan_solve``) mirrors the
``_solve_lax`` fixed point, which is itself parity-tested against
``_solve_python`` for the *standard*-closure, SB-graph-OFF path
(``test_iterative_lax.py``). So these end-to-end parity tests pin the reference
to that shared math path (``saha_boltzmann_graph=False``, ``stark_ne=False``,
``standard`` closure) where the composition comparison is exact. With
``CFLIBS_USE_LAX_WHILE_LOOP=1`` the reference uses the bit-exact lax oracle.

The full production-config solve (SB-graph + oxide + Stark-pinned n_e) is a
J8.5+ deliverable (``joint_wls_solve`` is the production estimator); the M1 gate
here is exact COMPOSITION parity on the shared solve math.

Data
----
Synthetic spectra are generated by the deterministic CF-LIBS forward model (no
external data). A real ChemCam BHVO-2 fixture is used when present
(``data/bhvo2_usgs/chemcam_bhvo2_loc1_spectrum.csv``, tracked in this
worktree); skipped gracefully if absent.

CPU x64 (conftest forces ``jax_enable_x64``); a narrow subset per the watchdog
rules in CLAUDE.md.
"""

from __future__ import annotations

import os
from pathlib import Path

import numpy as np
import pytest

os.environ.setdefault("JAX_PLATFORMS", "cpu")
# Pin the reference to the bit-exact lax fixed point that scan_solve mirrors.
os.environ.setdefault("CFLIBS_USE_LAX_WHILE_LOOP", "1")

jax = pytest.importorskip("jax")
jax.config.update("jax_enable_x64", True)

pytestmark = [pytest.mark.requires_jax, pytest.mark.requires_db, pytest.mark.integration]

DB_PATH = "ASD_da/libs_production.db"
_REPO_ROOT = Path(__file__).resolve().parents[2]
_REAL_SPECTRUM = _REPO_ROOT / "data" / "bhvo2_usgs" / "chemcam_bhvo2_loc1_spectrum.csv"

# Parity config: the shared solve math path (see module docstring).
_PARITY_OVERRIDES = {
    "closure_mode": "standard",
    "saha_boltzmann_graph": False,
    "stark_ne": False,
    "response_curve": None,
}


@pytest.fixture(scope="module")
def db():
    from cflibs.atomic.database import AtomicDatabase

    if not Path(DB_PATH).exists():  # pragma: no cover - env-dependent
        pytest.skip(f"atomic DB not present at {DB_PATH}")
    return AtomicDatabase(DB_PATH)


@pytest.fixture(scope="module")
def snapshot():
    from cflibs.jitpipe import build_snapshot

    if not Path(DB_PATH).exists():  # pragma: no cover
        pytest.skip(f"atomic DB not present at {DB_PATH}")
    return build_snapshot(DB_PATH)


def _static_for(snapshot, closure_mode="standard", max_iters=20):
    from cflibs.jitpipe import StaticConfig

    return StaticConfig(
        bucket_id=0,
        n_species=snapshot.n_species,
        level_pad=snapshot.level_pad[1],
        closure_mode=closure_mode,
        max_iters=max_iters,
    )


def _synthetic_spectrum(db, T_K=9000.0, n_e=2.0e16, lam=(240.0, 450.0), dlam=0.02):
    """Deterministic synthetic spectrum via the CF-LIBS forward model."""
    from cflibs.instrument.model import InstrumentModel
    from cflibs.plasma.state import SingleZoneLTEPlasma
    from cflibs.radiation.spectrum_model import SpectrumModel

    species = {"Fe": 5.0e15, "Ca": 3.0e15, "Ti": 2.0e15}
    plasma = SingleZoneLTEPlasma(T_e=T_K, n_e=n_e, species=species)
    instr = InstrumentModel(resolution_fwhm_nm=0.05)
    model = SpectrumModel(plasma, db, instr, lam[0], lam[1], dlam)
    wl, inten = model.compute_spectrum()
    return np.asarray(wl, float), np.asarray(inten, float), list(species.keys())


def _load_real_spectrum():
    rows = np.genfromtxt(_REAL_SPECTRUM, delimiter=",", names=True)
    return np.asarray(rows["wavelength"], float), np.asarray(rows["intensity"], float)


def _assert_m1_parity(ref, got, *, elements):
    """Assert the M1 tolerances between a reference and jit CFLIBSResult."""
    # 1. Element calls agree (every present element must match here).
    ref_calls = {k for k, v in ref.concentrations.items() if v > 0.0}
    got_calls = {k for k, v in got.concentrations.items() if v > 0.0}
    assert got_calls == ref_calls, f"element calls differ: jit={got_calls} ref={ref_calls}"

    # 2. T within 2 %, n_e within 10 %.
    np.testing.assert_allclose(got.temperature_K, ref.temperature_K, rtol=0.02)
    np.testing.assert_allclose(got.electron_density_cm3, ref.electron_density_cm3, rtol=0.10)

    # 3. Per-element concentration rtol 5 % / atol 0.01.
    for el in ref.concentrations:
        np.testing.assert_allclose(
            got.concentrations.get(el, 0.0),
            ref.concentrations[el],
            rtol=0.05,
            atol=0.01,
            err_msg=f"concentration parity failed for {el}",
        )


