# ---------------------------------------------------------------------------
# 3. Failure-policy parity (AC4): zero usable lines -> same all-FN record.
# ---------------------------------------------------------------------------


def test_failure_policy_parity(db, snapshot):
    """A flat (peak-free) spectrum: reference raises ValueError, jit returns all-FN.

    The reference scoreboard scores the ValueError as all false-negative; the
    jit pipeline must emit the same all-FN record (NaN-free, ``converged=False``,
    element keys preserved) — not a crash, not NaN.
    """
    from cflibs.inversion.pipeline import build_pipeline_config, run_pipeline
    from cflibs.jitpipe import PipelineParams, run_one

    wl = np.arange(240.0, 450.0, 0.02)
    flat = np.ones_like(wl)
    elements = ["Fe", "Ca", "Ti"]
    cfg = build_pipeline_config(elements, preset="raw", overrides=_PARITY_OVERRIDES)

    # Reference: zero observations -> ValueError.
    with pytest.raises(ValueError, match="No usable spectral lines"):
        run_pipeline(wl, flat, db, cfg)

    # Jit: same all-FN record (no crash, no NaN).
    got = run_one(
        flat,
        wl,
        snapshot,
        PipelineParams(),
        _static_for(snapshot),
        atomic_db=db,
        pipeline_config=cfg,
    )
    assert got.converged is False
    assert set(got.concentrations) == set(elements)
    assert all(v == 0.0 for v in got.concentrations.values())
    assert np.isfinite(got.temperature_K)
    assert np.isfinite(got.electron_density_cm3)
    assert not any(np.isnan(v) for v in got.concentrations.values())


